package com.restaurant.miscel;

public class Interval {
	
	private long starts;
	private long ends;
	
	public Interval(){}

	public long getStarts() {
		return starts;
	}

	public void setStarts(long starts) {
		this.starts = starts;
	}

	public long getEnds() {
		return ends;
	}

	public void setEnds(long ends) {
		this.ends = ends;
	}
	
	

}
